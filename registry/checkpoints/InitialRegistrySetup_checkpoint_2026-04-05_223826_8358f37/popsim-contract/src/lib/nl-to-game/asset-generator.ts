export const GeneratedAssetSchema = z.object({
    assetId: z.string().uuid(),
    moduleName: z.string(),
    assetType: z.enum(['luau_script', 'model', 'ui_component', 'datastore_schema', 'config']),
    content: z.string(),
    dependencies: z.array(z.string()),
    contractRef: z.string().uuid(),
    generatedAt: z.string().datetime(),
    agentId: z.string().uuid(),
    qualityScore: z.number().min(0).max(1),
    validationStatus: z.enum(['pending', 'validated', 'failed']),
});

export const AssetGenerationResultSchema = z.object({
    success: z.boolean(),
    assets: z.array(GeneratedAssetSchema),
    contractId: z.string().uuid(),
    runId: z.string(),
    totalModules: z.number(),
    completedModules: z.number(),
    failedModules: z.number(),
    executionTimeMs: z.number(),
    nextSteps: z.array(z.string()),
});

